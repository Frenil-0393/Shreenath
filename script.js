function storeOriginalTexts() {
  document.querySelectorAll("[data-i18n]").forEach(function(el) {
    var key = el.getAttribute("data-i18n");
    if (!originalTexts[key]) {
      originalTexts[key] = el.textContent;
    }
  });
}

function applyLanguage(lang) {
  var elems = document.querySelectorAll("[data-i18n]");
  elems.forEach(function(el) {
    var key = el.getAttribute("data-i18n");
    if (lang === "gu" && translations.gu[key]) {
      el.textContent = translations.gu[key];
    } else if (lang === "en" && originalTexts[key]) {
      el.textContent = originalTexts[key];
    }
  });

  // Update lang label
  if (langLabel) {
    langLabel.textContent = lang === "gu" ? "\u0A97\u0AC1" : "EN";
  }

  // Update html lang attribute
  root.setAttribute("lang", lang === "gu" ? "gu" : "en");
  root.setAttribute("data-lang", lang);

  // Update dropdown active state
  if (langMenu) {
    langMenu.querySelectorAll("button").forEach(function(btn) {
      btn.classList.toggle("active", btn.getAttribute("data-lang") === lang);
    });
  }

  // Update mobile preference active state
  document.querySelectorAll('.mobile-pref-menu button[data-lang]').forEach(function(btn) {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
  });

  localStorage.setItem(LANG_KEY, lang);
}

