// ─── THEME SYSTEM ──────────────────────────────────────────────
function getStoredThemeMode() {
  return localStorage.getItem(THEME_KEY) || "system";
}

function resolveTheme(mode) {
  if (mode === "dark") return "dark";
  if (mode === "light") return "light";
  return systemQuery.matches ? "dark" : "light";
}

function applyTheme(mode) {
  var finalTheme = resolveTheme(mode);
  root.setAttribute("data-theme", finalTheme);

  // Update desktop dropdown active state
  if (themeMenu) {
    var btns = themeMenu.querySelectorAll("button");
    btns.forEach(function(btn) {
      btn.classList.toggle("active", btn.getAttribute("data-theme-mode") === mode);
    });
  }

  // Update mobile preference active state
  document.querySelectorAll('.mobile-pref-menu button[data-theme-mode]').forEach(function(btn) {
    btn.classList.toggle('active', btn.getAttribute('data-theme-mode') === mode);
  });
}

function setThemeMode(mode) {
  localStorage.setItem(THEME_KEY, mode);
  applyTheme(mode);
}

